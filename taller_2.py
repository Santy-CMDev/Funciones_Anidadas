def bienvenida(edad):
    if edad < 18:
        print("Lamento informarte que eres menor de edad. No puedes acceder al programa.")
        return

    def pedirEdad():
        if edad >= 18:
            print("Bienvenido al programa, eres mayor de edad.")
        else:
            print(f"Lamento informarte que eres menor de edad. No puedes acceder al programa. Te faltan {18 - edad} años.")

    pedirEdad()

    def generacionPertenece():
        if 24 < edad < 40:
            print("Perteneces a la generación millennial.")
        elif 10 < edad < 24:
            print("Perteneces a la Generación Z.")
        elif 0 < edad < 10:
            print("Perteneces a la Generación Alfa.")
        else:
            print("Eres un Boomer.")

    generacionPertenece()

    def numeroRedondo():
        if edad % 10 == 0:
            print("Tu edad es un número redondo.")
            return True
        else:
            print("Tu edad no es un número redondo.")
            return False

    numeroRedondo()

