def agregar_contacto(agenda):
    nombre = input("Ingresa el nombre del contacto: ")
    telefono = input("Ingresa el teléfono del contacto: ")
    agenda[nombre] = telefono
    print(f"Contacto {nombre} agregado correctamente.")

def buscar_contacto(agenda):
    nombre = input("Ingresa el nombre del contacto que deseas buscar: ")
    if nombre in agenda:
        print(f"Teléfono de {nombre}: {agenda[nombre]}")
    else:
        print(f"{nombre} no está en la lista de contactos.")

def mostrar_contactos(agenda):
    print("Lista de contactos:")
    for nombre, telefono in agenda.items():
        print(f"{nombre}: {telefono}")

def editar_contacto(agenda):
    nombre = input("Ingresa el nombre del contacto que deseas editar: ")
    if nombre in agenda:
        nuevo_telefono = input("Ingresa el nuevo teléfono: ")
        agenda[nombre] = nuevo_telefono
        print(f"Teléfono de {nombre} actualizado correctamente.")
    else:
        print(f"{nombre} no está en la lista de contactos.")

def principal():
    agenda = {}

    while True:
        print("\n--- Agenda de Contactos ---")
        print("1. Agregar Contacto")
        print("2. Buscar Contacto")
        print("3. Mostrar Contactos")
        print("4. Editar Contacto")
        print("5. Salir")

        opcion = input("Elige una opción (1-5): ")

        if opcion == "1":
            agregar_contacto(agenda)
        elif opcion == "2":
            buscar_contacto(agenda)
        elif opcion == "3":
            mostrar_contactos(agenda)
        elif opcion == "4":
            editar_contacto(agenda)
        elif opcion == "5":
            print("¡Hasta luego!")
            break
        else:
            print("Opción inválida. Inténtalo de nuevo.")

if __name__ == "__main__":
    principal()

