def agregar_invitado(lista_invitados):
    nombre = input("Ingresa el nombre del invitado: ")
    lista_invitados.append(nombre)
    print(f"{nombre} ha sido añadido a la lista de invitados.")

def eliminar_invitado(lista_invitados):
    nombre = input("Ingresa el nombre del invitado que deseas eliminar: ")
    if nombre in lista_invitados:
        lista_invitados.remove(nombre)
        print(f"{nombre} ha sido eliminado de la lista de invitados.")
    else:
        print(f"{nombre} no está en la lista de invitados.")

def verificar_invitado(lista_invitados):
    nombre = input("Ingresa el nombre del invitado que deseas verificar: ")
    if nombre in lista_invitados:
        print(f"{nombre} está en la lista de invitados.")
    else:
        print(f"{nombre} no está en la lista de invitados.")

def editar_invitado(lista_invitados):
    nombre_actual = input("Ingresa el nombre del invitado que deseas editar: ")
    if nombre_actual in lista_invitados:
        nuevo_nombre = input("Ingresa el nuevo nombre: ")
        lista_invitados[lista_invitados.index(nombre_actual)] = nuevo_nombre
        print(f"{nombre_actual} ha sido actualizado a {nuevo_nombre}.")
    else:
        print(f"{nombre_actual} no está en la lista de invitados.")

